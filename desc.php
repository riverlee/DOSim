DOSim is developed on DO to 1) measure the similarity between diseases (DO terms), 2) measure the similarity between human genes in terms of diseases, 3) detect DO-driven gene modules and multilayer annotate them on dieases (DO), functions(GO) and pathways(KEGG), 4) conduct DO enrichment analysis, and 5) visualize and describe DO structures and terms. It focuses on the computation of disease similarity and gene similarity. Besides, its module detection and annotation would promote our understanding of the complex pathogenesis of diseases.
